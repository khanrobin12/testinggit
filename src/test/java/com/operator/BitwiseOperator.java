package com.operator;

/*it helps us to find binary logic. Bitwise operator are &,|,^,>>,<<,~,*/
public class BitwiseOperator {

	public static void main(String[] args) {
		
		
		int a=32;
		int b=12;
		
		int c;
		 c=a&b;
		 System.out.println(c);
		 
		 c=a|b;
		 System.out.println(c);
		 
		 c=a^b;
		 System.out.println(c);
		 
		 c=a>>b;
		 System.out.println(c);
		 
		 c=a<<b;
		 System.out.println(c);
				
		

	}

}
