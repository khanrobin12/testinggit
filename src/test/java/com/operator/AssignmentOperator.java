package com.operator;

public class AssignmentOperator {

	/* Assignment operators are =,+=,-=,*=,/=,%=. */
			
	public static void main(String[] args) {

		int x=7;
		int y=4;
		
		x+=y;
		System.out.println(x);
		
		x-=y;
		System.out.println(x);
		
		x*=y;
		System.out.println(x);
		
		x/=y;
		System.out.println(x);
		
		x%=y;
		System.out.println(x);
		
		
		

	}

}
