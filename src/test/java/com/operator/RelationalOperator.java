package com.operator;

public class RelationalOperator {
	
	/* Relational Oparator outcome will be false. Relational opetarot are <,<=,>,>=,==,!=, */

	public static void main(String[] args) {
		
		
		int a=10;
		int b=5;
		
		System.out.println(a<b);  //a less than b
		System.out.println(a<=b); //a less than = to b
		System.out.println(a>b); //a greater than b
		System.out.println(a>=b); // a greater than = to b
		System.out.println(a==b);// a equal to b  
		System.out.println(a!=b); // a not equal to b 

	}

}
