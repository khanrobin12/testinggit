package com.anisul_islam;

/*
a special character followed by backslash. 
\b ----backspace
\t ---- tab
\n ----newline
\r ---carriage return
\" ---double quote
\' --single quote
\\ -- backslash 
*/
public class EscapeSequence {

	public static void main(String[] args) {
		
		String a = "if \n\t you want to \b learn you \n have to work \"hard\" and \'never\' give \\up";
		
		System.out.println(a);
		
		

	}

}
