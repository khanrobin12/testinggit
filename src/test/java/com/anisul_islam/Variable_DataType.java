/*
Variable is container that store/hold value. 
we can be use as variable "a-zA-Z0-9 _,$"
can't start with digit first. 
java keyword can't be variable
*/

/*data type is the classification of data.
1.Primitive data type such as boolean, char, byte, short, int, long, float, double. 
2.Non-primitive data type such as String, Array.

 */

public class Variable_DataType {

	public static void main(String[] args) {
		
			int veriable =5;
			System.out.println(veriable);
			
			boolean bool = true;
			System.out.println(bool);
			char cha='a';
			System.out.println(cha);
			byte byt =65;
			System.out.println(byt);
			short shor=3456;
			System.out.println(shor);
			int in=2456678;
			System.out.println(in);
			long lon=254674256;
			System.out.println(lon);
			float fl = 23.5f; // if we want to use float data type we have to use f at the end.
			System.out.println(fl);
			double dou=657566;
			System.out.println(dou);
			


	}

}
