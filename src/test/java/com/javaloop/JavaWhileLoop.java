package com.javaloop;

/*while loop check the condition first then run the body. */

public class JavaWhileLoop {

	public static void main(String[] args) {
		
		
			int i=1;
			while (i<=10) {
				
				System.out.println(i+"           Bangladesh");
				i++;
			}
			
				System.out.println("Print all even number up to 10");
	
				int j=2;
					while (j<=10) {
						
						System.out.println(j+"           Bangladesh");
						j=j+2;
					}
				
			System.out.println("Print all odd number up to 10");

			int k=1;
				while (k<=10) {
					
					System.out.println(k+"           Bangladesh");
					k=k+2;
				}

				System.out.println("Print all odd number up to 10");
	
				int k1=1;
					while (k1<=10) {
						
						System.out.println(k1+"           Bangladesh");
						k1=k1+2;
					}
					
				System.out.println("Print decrease number up to 10");
				
				int l=10;
					while (l>=1) {
						
						System.out.println(l+"           Bangladesh");
						l--;
					}
	}

}
