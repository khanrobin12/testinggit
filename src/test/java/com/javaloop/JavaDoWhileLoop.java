package com.javaloop;

/*it doesn't matter if the condition is true or false it will run at list one time. 
*/
public class JavaDoWhileLoop {

	public static void main(String[] args) {

		int i=1;
		
		do {
			System.out.println(i+"   Bangladesh");
			
			i++;
			
		} while (i<=100);
		
		
		

	}

}
